
#1/bin/bash

LOGFILE="log_$(date +%F).log"

log() {
echo "[$(date '+%F %T')] $1" >> "$LOGFILE"
}


log "...............................................script started ....................................................."
log "...............................................checking the disk usage ............................................"
df -h >> "$LOGFILE"
log "................................................Disk check complete..............................................."
log "................................................Script Ended .... Thank you....................................... "

